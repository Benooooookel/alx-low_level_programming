#include "3-calc.h"

/**
 * op_add - function that adds value a to value b
 * @a: the first operator
 * @b: the second operator
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtract value a and b
 * @a: the first operator
 * @b: the second operator
 * Return: the sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that subtract value a and b
 * @a: the first operator
 * @b: the second operator
 * Return: the sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that multiply value a and b
 * @a: the first operator
 * @b: the second operator
 * Return: the sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that modlus the value a and b
 * @a: the first operator
 * @b: the second operator
 * Return: the sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
